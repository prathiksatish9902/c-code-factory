#include <iostream>

using namespace std;

int main()
{
    int variable = 8;
    cout << variable << endl;
    variable = 20;
    cout << variable << endl;

    char a = 'A';
    cout << a << endl;

    a = 65;
    cout << a << endl;

    float b = 5.5f;
    cout<<b<<endl;

    double c = 6.4;
    cout<<c<<endl;

    bool var = 'A';
    cout<<var<<"hi"<<endl;
    var = false;
    cout<<var<<endl;

    cout<<"Int"<<sizeof(int)<<endl;
    cout<<"float"<<sizeof(float)<<endl;
    cout<<"char"<<sizeof(char)<<endl;
    cout<<"bool"<<sizeof(bool)<<endl;
    cout<<"double"<<sizeof(double)<<endl;





    return 0;
}
